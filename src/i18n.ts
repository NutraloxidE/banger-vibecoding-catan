const en: Record<string, string> = {};
const ja: Record<string, string> = {};
for (const k in M) { en[k] = M[k][0]; ja[k] = M[k][1]; }
export const DICTS: Record<Lang, Record<string, string>> = { en, ja };

let activeLang: Lang = 'en';
export function setActiveLang(l: Lang) { activeLang = l; }
export function getActiveLang(): Lang { return activeLang; }

export function t(key: string, params?: Record<string, string | number>, lang?: Lang): string {
  const dict = DICTS[lang ?? activeLang] ?? en;
  let s = dict[key] ?? en[key] ?? key;
  if (params) {
    for (const p in params) s = s.split(`{${p}}`).join(String(params[p]));
  }
  return s;
}
